LIST P=16F877
CUENTA 	EQU	20H
#INCLUDE <P16F877.INC>
	ORG	00H
	GOTO INICIO
	ORG	04H
	GOTO INTERRUPTO
	ORG 06H
INICIO	BSF	STATUS,5
		CLRF TRISB
		BCF	 STATUS,5
		MOVLW B'00000001'
		MOVWF T1CON
		BCF	PIR1,0
		BSF	STATUS,5
		BSF	PIE1,0;
		BCF	STATUS,5;
		BSF	INTCON,6
		BSF	INTCON,7
		BCF	STATUS,5
LOOP	MOVLW 0XFF
		MOVWF	PORTB
   		MOVLW 	0X05;254 FA
		MOVWF	CUENTA
RETARDO	NOP
		GOTO RETARDO
INTERRUPTO
		DECFSZ CUENTA,1
		GOTO UNO
		GOTO STOP
STOP
		MOVLW	0X00
		MOVWF	PORTB
UNO
		BCF PIR1,0
		RETFIE; !
		END

		

		
	