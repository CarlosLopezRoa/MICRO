LIST P=16F877
;Este programa es el semaforo con 5 segundos de rojo 2 intermitentes de amarillo y 3 de verde
#include <P16F877.INC>
	CON0 	EQU 20H
	CON1 	EQU 21H
	CON2 	EQU 22H
	ORG 	0X00
	GOTO 	START
	ORG 	0X10
START
	BSF 	STATUS, RP0
	MOVLW 	0X00
	MOVWF 	TRISB
	MOVWF	TRISD
	BCF	STATUS, RP0
INICIO
	MOVLW	B'00110000';VERDE ROJO
	MOVWF	PORTB
	CALL DELAY
	CALL DELAY
	CALL DELAY
	CALL DELAY
	CALL DELAY
	CALL DELAY
	MOVLW 	B'01010000'; AMARILLO ROJO
	MOVWF	PORTB
	CALL DELAY
	MOVLW 	B'00010000'
	MOVWF 	PORTB
	CALL DELAY
	MOVLW	B'01010000'
	MOVWF	PORTB
	CALL DELAY
	MOVLW	B'00010000'
	MOVWF	PORTB
	CALL DELAY
	MOVLW	B'10000100'; ROJO, VERDE
	MOVWF	PORTB
	CALL DELAY
	CALL DELAY
	CALL DELAY
	CALL DELAY
	CALL DELAY
	CALL DELAY
	MOVLW 	B'10001000'; ROJO, AMARILLO
	MOVWF 	PORTB
	CALL DELAY
	MOVLW 	B'10000000'
	MOVWF PORTB
	CALL DELAY
	MOVLW	B'10001000'
	MOVWF	PORTB
	CALL DELAY
	MOVLW	B'10000000';
	MOVWF PORTB
	CALL DELAY
	GOTO INICIO
	
	
	




	
DELAY
	MOVLW 0X0B
	MOVWF CON2
LOOP3
	DECFSZ CON2
	GOTO FOR2
	GOTO FIN
FOR2
	MOVLW 0XC4
	MOVWF CON0
LOOP
	DECFSZ 	CON0
	GOTO 	FOR1
	GOTO 	LOOP3
FOR1	
	MOVLW 0XFF
	MOVWF CON1
LOOP2
	DECFSZ	CON1
	GOTO LOOP2
	GOTO LOOP
FIN
	RETURN
END
	